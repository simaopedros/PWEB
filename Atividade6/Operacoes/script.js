function obternumero(){
	let numero01 = prompt("Digite o Primeiro Numero");	
	let numero02 = prompt("Digite o Segundo Numero");
	
	document.write('<hr><br> A Soma é: ' + (+numero01 + +numero02));
	document.write('<hr><br> A Subtracao é: ' + (+numero01 - +numero02));
	document.write('<hr><br> A divisão é: ' + (+numero01 / +numero02));
	document.write('<hr><br> A Multiplicacao é: ' + (+numero01 * +numero02));
	document.write('<hr><br> O Modulo é: ' + (+numero01 % +numero02));

}



